interface Props {
  platform?: string;
  size: number;
  color: string;
}

export function PlatformIcon({ platform, size, color }: Props) {
  let svg: string | null = null;

  switch (platform) {
    case 'darwin':
      svg = APPLE_SVG;
      break;
    case 'win32':
      svg = WINDOWS_SVG;
      break;
    case 'linux':
      svg = LINUX_SVG;
      break;
  }

  if (!svg) {
    return <Monitor size={size} color={color} />;
  }

  const colored = svg.replace('<path', `<path fill="${color}"`);
  return <SvgXml xml={colored} width={size} height={size} />;
}
